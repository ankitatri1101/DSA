class Solution {

  public:
    long long findMaxSubsetSum(int N, vector<int> &A) {
        vector<int> prev = {0, A[0]}, curr = {0, 0};
        
        for(long long i = 1; i < N; i++)
        {
            curr[0] = prev[1];
            curr[1] = A[i] + (*max_element(prev.begin(), prev.end()));
            prev = curr;
        }
        return *max_element(prev.begin(), prev.end());
    }
};


